import fs from "node:fs";
import path from "node:path";

const screenshotsDir = path.resolve("public/img/screenshots");
const outputFile = path.resolve("src/data/showcaseScreenshots.js");
const imageExtensions = new Set([".png", ".jpg", ".jpeg", ".webp", ".avif", ".gif", ".svg"]);

const files = fs.existsSync(screenshotsDir)
  ? fs
      .readdirSync(screenshotsDir, { withFileTypes: true })
      .filter((entry) => entry.isFile())
      .map((entry) => entry.name)
      .filter((filename) => imageExtensions.has(path.extname(filename).toLowerCase()))
      .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }))
  : [];

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(
  outputFile,
  [
    "// This file is auto-generated by scripts/generate-screenshot-manifest.mjs.",
    "// Drop images into public/img/screenshots, then run dev/build again.",
    `export const showcaseScreenshotFiles = ${JSON.stringify(files, null, 2)};`,
    ""
  ].join("\n")
);

console.log(`Generated ${files.length} showcase screenshots.`);
